#===============================================================================
#  `String` class extensions
#===============================================================================
class ::String
  #-----------------------------------------------------------------------------
  #  turns string into an actual Ruby object
  #-----------------------------------------------------------------------------
  def constantize
    Object.const_get(self)
  end
  #-----------------------------------------------------------------------------
  #  checks if string is in URL format
  #-----------------------------------------------------------------------------
  def url?
    self.scan(/https?:\/\/(www\.)?[-a-zA-Z0-9@:%._\+~#=]{1,256}\.[a-zA-Z0-9()]{1,6}\b([-a-zA-Z0-9()!@:%_\+.~#?&\/\/=]*)/).count > 0
  end
  #-----------------------------------------------------------------------------
  #  checks if string contains only numeric values
  #-----------------------------------------------------------------------------
  def numeric?
    return self.scan(/^[+-]?([0-9]+)(?:\.[0-9]+)?$/).count > 1
  end
  #-----------------------------------------------------------------------------
end
