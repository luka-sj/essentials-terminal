#===============================================================================
#  Registry module for running commands
#===============================================================================
module Commands
  module Registry
    class << self
      #-------------------------------------------------------------------------
      #  get commands variable
      #-------------------------------------------------------------------------
      def commands
        @commands ||= {}
      end
      #-------------------------------------------------------------------------
      #  register valid command
      #-------------------------------------------------------------------------
      def register(name, command)
        commands[name] = command
      end
      #-------------------------------------------------------------------------
      #  check if command has been registered
      #-------------------------------------------------------------------------
      def command?(command)
        commands.key?(command)
      end
      #-------------------------------------------------------------------------
      #  get command action from alias
      #-------------------------------------------------------------------------
      def from_alias(name)
        commands[name]
      end
      #-------------------------------------------------------------------------
    end
  end
end
