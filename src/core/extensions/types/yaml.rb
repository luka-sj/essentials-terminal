#===============================================================================
#  `YAML` class extensions
#===============================================================================
module Psych
  class << self
    #---------------------------------------------------------------------------
    #  parse .yml into a Ruby hash
    #---------------------------------------------------------------------------
    def parse_hash(file)
      yaml = ::YAML.load_file(file)
      erb  = ::ERB.new(yaml.inspect)

      ::JSON.parse(erb.result(binding).gsub('=>', ':'))
    end
    #---------------------------------------------------------------------------
  end
end
